CREATE OR REPLACE FUNCTION public.fn_enforce_lifecycle_transition()
RETURNS trigger LANGUAGE plpgsql AS $$
DECLARE
  actor_role project_role;
BEGIN
  IF OLD.lifecycle_state IS NOT DISTINCT FROM NEW.lifecycle_state THEN
    RETURN NEW;
  END IF;

  SELECT role INTO actor_role
  FROM project_users
  WHERE user_id = auth.uid()
    AND project_id = NEW.project_id
    AND invite_status = 'accepted'
  LIMIT 1;

  IF NOT EXISTS (
    SELECT 1 FROM role_stage_transitions
    WHERE from_state = OLD.lifecycle_state
      AND to_state   = NEW.lifecycle_state
      AND actor_role = ANY(allowed_roles)
  ) THEN
    RAISE EXCEPTION 'invalid_role_lifecycle_transition: % → % by %',
      OLD.lifecycle_state, NEW.lifecycle_state, actor_role
      USING ERRCODE = '22000';
  END IF;

  RETURN NEW;
END $$;


