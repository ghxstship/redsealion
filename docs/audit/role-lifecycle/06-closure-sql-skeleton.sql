-- │ C-STATE-02 — Closeout immutability trigger              │
-- └─────────────────────────────────────────────────────────┘

CREATE OR REPLACE FUNCTION public.fn_enforce_closeout_immutability()
RETURNS trigger LANGUAGE plpgsql AS $$
DECLARE
  override_reason text := current_setting('app.closeout_override_reason', true);
  platform_role text;
BEGIN
  IF TG_OP = 'UPDATE' AND OLD.lifecycle_state = 'closeout' THEN
    SELECT role INTO platform_role FROM org_users
      WHERE user_id = auth.uid() AND role = 'developer' LIMIT 1;
    IF platform_role IS NULL OR override_reason IS NULL THEN
      RAISE EXCEPTION 'closeout_immutable' USING ERRCODE = '22000';
    END IF;
  END IF;
  RETURN NEW;
END $$;


