CREATE OR REPLACE FUNCTION public.fn_log_role_transition()
RETURNS trigger LANGUAGE plpgsql AS $$
BEGIN
  IF OLD.lifecycle_state IS DISTINCT FROM NEW.lifecycle_state THEN
    INSERT INTO project_role_events (project_id, user_id, role,
                                     from_state, to_state,
                                     actor_id, actor_role)
    VALUES (NEW.project_id, NEW.user_id, NEW.role,
            OLD.lifecycle_state, NEW.lifecycle_state,
            auth.uid(),
            COALESCE((SELECT role::text FROM project_users
                      WHERE user_id = auth.uid()
                        AND project_id = NEW.project_id LIMIT 1),
                     'platform'));
  END IF;
  RETURN NEW;
END $$;

CREATE TRIGGER trg_project_users_log_transition
  AFTER UPDATE OF lifecycle_state ON public.project_users
  FOR EACH ROW EXECUTE FUNCTION fn_log_role_transition();


